void LCD_RefreshScore()
{
	char txt[10];
	static int oldLength;
	int i,lnt;
	UG_FontSelect(&RFONT_8X12);
	UG_SetForecolor(C_WHITE);
	sprintf(txt,"%d",score);
	lnt=strlen(txt);
	if (lnt<oldLength)
	{
		for (i=0;i<oldLength-lnt; i++) txt[lnt+i]=' ';
		txt[lnt+i]=0;	//termination char
	}
	UG_PutString(56,2,txt);
	oldLength=lnt;
}

void LCD_RefreshLives()
{
	static int oldLives;
	const int lX[3]={271,286,301};
	int i, clives;
			
/*	if (lives > 9)	//GREEN
	else if (lives > 6)	//BLUE
	else if (lives > 3)	//YELLOW
	else 	//RED
*/
	clives=lives;	//copy number of lives first, then adjust
					//so we don't try to draw more than possible
	if (lives == -1)	//force refresh
	{
		lives=0;
		clives=0;
		oldLives=3;
	}
	else
	{
		if (clives > 3) clives=3;
		if (clives < 0) clives=0;
		if (clives == oldLives) return;
	}
	if (clives > oldLives)	//add lives
	{
		for (i=oldLives; i<clives; i++ )
		{
			DrawIcon_P(lX[i],17,14,11,C_BLACK,C_RED,&lifeIco[0][0]);
		}	
	}
	else	//remove lives
	{
		for (i=clives; i<oldLives; i++ )
		{
			UG_FillFrame(lX[i],17,lX[i]+14,17+11,C_BLACK);
		}
	}
	oldLives=clives;
}

