void BackgroundServices()
{
	static uint32_t last_time;
	
	if (Has_X_MillisecondsPassed(10,&last_time))
	{
		KBD_Read();
	}
	RefreshLCDandLEDs();
}

void RefreshLCDandLEDs()
{
	switch (display_msg)
	{
		case DISP_MOLE1:
//			LED_ShowMoleNr(1);
			LCD_ShowMoleNr(1);
			break;
		case DISP_MOLE2:
//			LED_ShowMoleNr(2);
			LCD_ShowMoleNr(2);
			break;
		case DISP_MOLE3:
//			LED_ShowMoleNr(3);
			LCD_ShowMoleNr(3);
			break;
		case DISP_MOLE4:
//			LED_ShowMoleNr(4);
			LCD_ShowMoleNr(4);
			break;
		case DISP_MOLE_NONE:
//			LED_ShowMoleNr(0);
			LCD_ShowMoleNr(0);
			break;
		case DISP_MOLE_ALL:
//			LED_ShowMoleNr(5);
			LCD_ShowMoleNr(5);
			break;
		case DISP_REFRESH_SCORE:
			LCD_RefreshScore();
			break;
		case DISP_REFRESH_LEVEL:
			LCD_RefreshLevel();
			break;
		case DISP_REFRESH_LIVES:
			LCD_RefreshLives();
			break;
		case DISP_REFRESH_ALL:
			LCD_RefreshScore();
			LCD_RefreshLevel();
			LCD_RefreshLives();
			break;
		case DISP_START_SCREEN:
			DrawStartScreen();
			break;
		case DISP_GAME_PLAY_SCREEN:
			DrawGamePlayScreen();
			LCD_RefreshScore();
			LCD_RefreshLevel();
			LCD_RefreshLives();
			break;
		case DISP_END_SCREEN:
			lives = -1;
			LCD_RefreshLives();
			DrawGameOver();
			break;
		case DSIP_END_SCORE:
			DrawGameScore();
			break;
		default:	//0 or unknown - do nothing
		break;
	}
	display_msg=0;
}

