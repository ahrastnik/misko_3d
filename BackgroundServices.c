void LCD_ShowMoleNr(char mole_nr)
{
	static char oldMolePositions;	//bits 0-3: 1-mole is drawn, 0-mole isn't drawn
	const int mX[4]={80-15,200-15,280-15,200-15};
	const int mY[4]={120-15,120-15,160-15,200-15};
	int i;
	
	if (mole_nr == 0 || mole_nr == 6)	//delete all moles (6 forces delete regardless of oldMolePositions )
	{
		for (i=0; i<4; i++)
		{
			if ((mole_nr == 6) || (oldMolePositions & (1<<i)))	//if present delete else do nothiing
			{
				UG_FillCircle(mX[i]+15,mY[i]+15,15,C_BLACK);
			}
		}
		oldMolePositions=0;
	}
	else if (mole_nr == 5) //draw all moles
	{
		for (i=0; i<4; i++)
		{
			if (!(oldMolePositions & (1<<i)))	//if !present draw else do nothiing
			{
				DrawSprite_P(mX[i],mY[i],30,31,C_GREEN,&sprite[0][0]);
			}
		}
		oldMolePositions=0x0F;
	}
	else if ((mole_nr<5) && (mole_nr>0))	//delete moles on the wrong positions and draw the mole on the correct position
	{
		for (i=0; i<4; i++)
		{
			if ((!(oldMolePositions & (1<<i))) && ((i+1) == mole_nr))	//if !present && needs to be here, draw 
			{
				DrawSprite_P(mX[i],mY[i],30,31,C_GREEN,&sprite[0][0]);
				oldMolePositions |= (1<<i);
			}
			else if ((oldMolePositions & (1<<i)) && ((i+1) != mole_nr))	//else if present and mustn't be, delete else do nothiing
			{
				UG_FillCircle(mX[i]+15,mY[i]+15,15,C_BLACK);
				oldMolePositions &= ~(1<<i);
			}
		}
	}
}

